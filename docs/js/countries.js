const byCode = new Map(COUNTRIES.map((c) => [c.code, c]));

export function countryByCode(code) {
  return byCode.get((code ?? "").toUpperCase()) ?? null;
}

// Longitude span, accounting for antimeridian-crossing boxes.
function widthDeg([, west, , east]) {
  const w = east - west;
  return w < 0 ? w + 360 : w;
}

// The smallest country whose bbox contains the point, so Singapore beats
// Malaysia/Indonesia (whose boxes contain it). Null when no box matches.
export function detectCountry(lat, lng) {
  let best = null;
  let bestArea = Infinity;
  for (const country of COUNTRIES) {
    if (!inBounds(lat, lng, country.bbox)) continue;
    const area = (country.bbox[2] - country.bbox[0]) * widthDeg(country.bbox);
    if (area < bestArea) {
      best = country;
      bestArea = area;
    }
  }
  return best;
}
